; Flips a string and rotates it 4 times

INCLUDE Irvine32.inc

.386
.model flat,stdcall
.stack 4096
ExitProcess proto,dwExitCode:dword
.data
	source BYTE "This is the source string", 0
	target BYTE SIZEOF source DUP('#')
.code
;performs the calculation
main proc
	
	
	mov	esi,0	;point to first element

	mov edi, LENGTHOF source-1	;point to last element

	mov ecx, LENGTHOF source	;counter so that we loop through all the spots of the array

	;ecx is automatically decrimated

	SwapLoop:	;top of loop
		mov eax,0
		mov al,source[esi]	;instruction operands must be the same size so we are going to use the first half of the eax register
		mov target[edi],al	;moves the first(this will be incremented) value of the string to the last(this value will be decremented) spot in the array

		inc esi ;moves pointer forward
		dec edi	;moves pointer backward
		call dumpregs

		loop SwapLoop


	mov ecx,4 ;counter so that we only rotate the string 4 times
	RepeatLoop: ;calls the rotate loop 4 times
		mov edx, ecx
		
		mov ecx, SIZEOF target -1 ;sets the counter to the size of the array for the rotate loop
		mov esi, 0	
		mov al,target[0]
		RotateLoop:	;takes the last element of the array and moves it to the start
				
			mov bl,target[esi+1];store next spot in a temp variable
			mov target[esi+1],al;move stored variable into next spot
				
			mov al,bl;store temp var into not temp varable
			call dumpregs
			inc esi
			loop RotateLoop
		mov target[0],bl;move the last var to the first spot
		call dumpregs
		mov ecx,edx	;sets the counter back to the repeat loop counter
		loop RepeatLoop

	invoke ExitProcess,0
main endp
end main

